package OOP_2_1;
//Конструкторы
public class Box5 {
    double length;
    double width;
    double height;
    Box5() {
        length = -1;
        width = -1;
        height = -1;
    }
    Box5(double length, double width, double height) {
        this.length = length;
        this.width = width;
        this.height = height;
    }
}
